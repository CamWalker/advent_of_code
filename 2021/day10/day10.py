points = {
	')': 1,
	']': 2,
	'}': 3,
	'>': 4,
	None: 0,
}

pairings = {
	'(': ')',
	'[': ']',
	'{': '}',
	'<': '>',
}

def remove_good(text):
	old = ''
	new = text
	while old != new:
		old = new
		new = new.replace('()', '')
		new = new.replace('[]', '')
		new = new.replace('{}', '')
		new = new.replace('<>', '')
	return new

def find_first_corrupt(text):
	for char in text:
		if char in ')]}>':
			return char
	return None

def get_part2_score(clean_text):
	chars = []
	score = 0
	for char in clean_text:
		chars.insert(0, pairings[char])
	for char in chars:
		score *= 5
		score += points[char]
	return score

def main(lines):
	count = 0
	scores = []
	for text in lines.split('\n'):
		clean_text = remove_good(text)
		c = find_first_corrupt(clean_text)
		# count += points[c] # part 1
		if c == None:
			scores.append(get_part2_score(clean_text))
	
	scores.sort()
		
	return scores[math.floor(len(scores) / 2)]

